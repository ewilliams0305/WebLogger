namespace WebLogger.Render
{
    /// <summary>
    /// Defined elements supported by the framework
    /// </summary>
    public enum Element
    {
        /// <summary>
        /// Paragraph
        /// </summary>
        Paragraph,
        /// <summary>
        /// Header
        /// </summary>
        Header,
        /// <summary>
        /// H1 Large Text
        /// </summary>
        H1,
        /// <summary>
        /// H2 Large Text
        /// </summary>
        H2,
        /// <summary>
        /// H3 Large Text
        /// </summary>
        H3,
        /// <summary>
        /// H4 Large Text
        /// </summary>
        H4,
        /// <summary>
        /// H5 Large Text
        /// </summary>
        H5,
        /// <summary>
        /// Section
        /// </summary>
        Div,
        /// <summary>
        /// Span Element
        /// </summary>
        Span,
        /// <summary>
        /// Creates a table
        /// </summary>
        Table,
        /// <summary>
        /// Creates a table Header
        /// </summary>
        TableHeader,
        /// <summary>
        /// Creates a Row in a Table
        /// </summary>
        TableRow,
        /// <summary>
        /// Creates content in a Table
        /// </summary>
        TableData,
    }
}